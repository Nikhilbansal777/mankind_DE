-- Reconciliation check: cart.subtotal vs SUM(cart_item.quantity * cart_item.price)
-- Should return zero rows if everything reconciles.
SELECT 
    c.id,
    c.subtotal AS cart_subtotal,
    SUM(ci.quantity * ci.price) AS items_subtotal,
    c.subtotal - SUM(ci.quantity * ci.price) AS difference
FROM cart c
JOIN cart_item ci ON ci.cart_id = c.id
GROUP BY c.id, c.subtotal
HAVING c.subtotal <> SUM(ci.quantity * ci.price);
GO

