-- ============================================================
-- CREATE CART TABLE
-- ============================================================
CREATE TABLE cart (
    id INT PRIMARY KEY,
    user_id INT,
    session_id VARCHAR(100),
    status VARCHAR(20),  -- ACTIVE, CONVERTED, ABANDONED, REMOVED
    subtotal DECIMAL(10,2),
    tax DECIMAL(10,2),
    total DECIMAL(10,2),
    created_at DATETIME,
    updated_at DATETIME
);
GO

-- ============================================================
-- CREATE CART_ITEM TABLE
-- ============================================================
CREATE TABLE cart_item (
    id INT PRIMARY KEY,
    cart_id INT,
    product_id INT,
    product_name VARCHAR(100),
    quantity INT,
    price DECIMAL(10,2),
    FOREIGN KEY (cart_id) REFERENCES cart(id)
);
GO

